public class Main1 {
    public static void main(String[] args) {
        add();
        subtraction();
        multiplication();
        division();

    }

    public static void add() {
        int number0 = 70;
        int number1 = 36;

        int resultOfAddition = number0 + number1;

        System.out.println(resultOfAddition);
    }

    public static void subtraction() {
        int number0 = 80;
        int number1 = 25;

        int resultOfSubtraction = number0 - number1;

        System.out.println(resultOfSubtraction);

    }

    public static void multiplication() {
        int number0 = 240;
        int number1 = 7;

        int resultOfMultiplication = number0 * number1;

        System.out.println(resultOfMultiplication);

    }

    public static void division() {
        int number0 = 8;
        int number1 = 2560;

        int resultOfDivision = number1 / number0;

        System.out.println(resultOfDivision);

    }
}
